	MOV	AL,FF
	OUT	04
	MOV	AL,01
	MOV	BL,01
	OUT	04

START:
	IN	00		
	CMP	AL,32
	JZ	DOWN
	CMP	AL,34
	JZ	LEFT
	CMP	AL,36
	JZ	RIGHT
	CMP	AL,38
	JZ	UP
	CMP	AL,39
	JZ	SPEEDUP
	CMP	AL,33
	JZ	SPEEDDOWN
	JNZ	START

DOWN:
	MOV	AL,00
	ADD	AL,BL
	OR	AL,40
	OUT	04
	JMP	START

RIGHT:
	MOV	AL,00
	ADD	AL,BL
	OR	AL,10
	OUT	04
	JMP	START
LEFT:
	MOV	AL,00
	ADD	AL,BL
	OR	AL,20
	OUT	04
	JMP	START
UP:
	MOV	AL,00
	ADD	AL,BL
	OR	AL,80
	OUT	04
	JMP	START
SPEEDUP:
	INC	BL
	JMP	START
SPEEDDOWN:
	DEC	BL
	JMP	START
	
		

END
		